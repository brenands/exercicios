"""
Duas constantes - True e False
Sempre iniciadas por maiúscula
"""
# Negação (not), se o valor for verdadeiro, será False, se for falso, será True. Sempre ao contrário

ativo = False
print(not ativo)

# Ou (or), algum valor precisa ser verdadeiro
"""
True or True = True
True or False = True
False or True = True
False or False = False
"""

logado = False
print(ativo or logado)

# E (and), ambos os valores devem ser verdadeiros
"""
True or True = True
True or False = False
False or True = False
False or False = False
"""
